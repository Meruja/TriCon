package TriCon.model;


public class Journal
{
    private String journalId;
    private String studentId;
    private String progressId;
    private String lecturerId;
    private String industrialistId;
    private String company;
    private String field;
    private String category;
    private String contractNo;
    private String from;
    private String to;
    private String finalReportId;

    public String getStudentId() {
        return studentId;
    }

    public void setStudentId(String studentId) {
        this.studentId = studentId;
    }

    public String getProgressId() {
        return progressId;
    }

    public void setProgressId(String progressId) {
        this.progressId = progressId;
    }

    public String getLecturerId() {
        return lecturerId;
    }

    public void setLecturerId(String lecturerId) {
        this.lecturerId = lecturerId;
    }

    public String getIndustrialistId() {
        return industrialistId;
    }

    public void setIndustrialistId(String industrialistId) {
        this.industrialistId = industrialistId;
    }

    public String getField() {
        return field;
    }

    public void setField(String field) {
        this.field = field;
    }

    public String getCompany() {
        return company;
    }

    public void setCompany(String company) {
        this.company = company;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getContractNo() {
        return contractNo;
    }

    public void setContractNo(String contractNo) {
        this.contractNo = contractNo;
    }

    public String getFrom() {
        return from;
    }

    public void setFrom(String from) {
        this.from = from;
    }

    public String getTo() {
        return to;
    }

    public void setTo(String to) {
        this.to = to;
    }

    public String getJournalId() {
        return journalId;
    }

    public void setJournalId(String journalId) {
        this.journalId = journalId;
    }

    public String getFinalReportId() {
        return finalReportId;
    }

    public void setFinalReportId(String finalReportId) {
        this.finalReportId = finalReportId;
    }
}
